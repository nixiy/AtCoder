var sc = bufio.NewScanner(os.Stdin)

func ni() int {
	sc.Scan()
	i, _ := strconv.Atoi(sc.Text())
	return i
}

func abs(x int) int {
	if x > 0 {
		return x
	}
	return x * -1
}

// ex chmin(&p, v)
func chmin(p *int, v int) {
	if *p > v {
		*p = v
	}
}

